    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        SimpleDateFormat dateFormat = new SimpleDateFormat("yyyy-MM-dd");
        String tanggal = dateFormat.format(tmasuk.getDate());
        String JenisKendaraan = "";
        if (jmobil.isSelected()) {
            JenisKendaraan = "Mobil";
        }
        if (jmotor.isSelected()) {
            JenisKendaraan = "Motor";
        }
        
        if(tpetugas.getText().equals("") || tpolisi.getText().equals("") || JenisKendaraan.equals("") ||
           tmasuk.equals("") || tkeluar.equals("")) {
        } else {
            String SQL = "INSERT INTO tb_transaksi VALUES (NULL,'"+tpetugas.getText()+"','"+tpolisi.getText()+"','"+JenisKendaraan+"','"+
                    "','"+tmasuk+"','"+tkeluar+"','"+ttarif+",)";
            int status = KoneksiDB.execute(SQL);
            if (status == 1){
                JOptionPane.showMessageDialog(this, "Data berhasil ditambahkan","SUKSES",JOptionPane.INFORMATION_MESSAGE);
                selectData();
            } else {
                JOptionPane.showMessageDialog(this, "Data berhasil ditambahkan","SUKSES",
                        JOptionPane.WARNING_MESSAGE);
            }
        }
        
    }//GEN-LAST:event_jButton1ActionPerformed

