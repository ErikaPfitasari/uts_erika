    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
        // TODO add your handling code here:
        int baris = tblBismillah.getSelectedRow();
        if (baris != -1) {
            String NoResi = tblBismillah.getValueAt(baris, 0).toString();
            String SQL = "DELETE FROM tb_transaksi WHERE NoResi='"+NoResi+"'";
 
            int status = KoneksiDB.execute(SQL);
            if (status==1) {
                JOptionPane.showMessageDialog(this, "Data berhasil dihapus","Sukses",JOptionPane.INFORMATION_MESSAGE);
            } else {
                JOptionPane.showMessageDialog(this, "Pilih baris data terlebih dahulu","Error",JOptionPane.WARNING_MESSAGE);
            }
        }
    }//GEN-LAST:event_jButton3ActionPerformed

    private void tblBismillahMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tblBismillahMouseClicked
        // TODO add your handling code here:
        int baris = tblBismillah.getSelectedRow();
        if (baris != -1) {
            tpetugas.setText(tblBismillah.getValueAt(baris, 1).toString());
            tpolisi.setText(tblBismillah.getValueAt(baris, 2).toString());
            if ("Mobil".equals(tblBismillah.getValueAt(baris, 3).toString())) {
                jmobil.setSelected(true);
            } else {
                jmotor.setSelected(true);
            }
            ttarif.setText(tblBismillah.getValueAt(baris, 4).toString());
        }
    }//GEN-LAST:event_tblBismillahMouseClicked

