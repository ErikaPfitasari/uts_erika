    private void selectData() {
         String kolom[] = {"NoResi","NamaPetugas","NoPolisi","JenisKendaraan","TglMasuk","TglKeluar","Tarif"};
        DefaultTableModel dtm = new DefaultTableModel (null, kolom);
        String SQL = "SELECT * FROM tb_transaksi";
        ResultSet rs = KoneksiDB.executeQuery(SQL);
        try {
            while(rs.next()) {
                String NoResi = rs.getString(1);
                String NamaPetugas = rs.getString(2);
                String NoPolisi = rs.getString(3);
                String JenisKendaraan = "";
                if ("Mobil".equals(rs.getString(4))) {
                    JenisKendaraan = "Mobil";
                } else {
                    JenisKendaraan = "Motor";
                }
                String TglKeluar = rs.getString(5);
                String TglMasuk = rs.getString(6);
                String Tarif = rs.getString(7);
                String data[] = {NoResi,NamaPetugas,NoPolisi,JenisKendaraan,TglMasuk,TglKeluar,Tarif};
                dtm.addRow(data);
            }
        } catch (SQLException ex) {
            Logger.getLogger(frame1.class.getName()).log(Level.SEVERE, null, ex);
        }
        tblBismillah.setModel(dtm);
}   
        
